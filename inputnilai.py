n1 = int(input("masukan nilai pertama:\n"))
n2 = int(input("masukan nilai kedua:\n"))

plus = n1 + n2
print(n1, "+", n2, "=", plus)

mines = n1 - n2
print(n1, "-", n2, "=", mines)

kali = n1 * n2
print(n1, "*", n2, "=", kali)

pembagian = n1 / n2
print(n1, "/", n2, "=", pembagian)

x2 = n1 ** n2
print(n1, "**", n2, '=', x2)

bulatkan = n1 // n2
print(n1, "//", n2, "=", bulatkan)
