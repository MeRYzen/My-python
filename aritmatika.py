nilai1 = 10
nilai2 = 8

# penjumlahan
penjumlahan = nilai1 + nilai2
print(nilai1, "+", nilai2, "=", penjumlahan)

# pengurangan
pengurangan = nilai1 - nilai2
print(nilai1, "-", nilai2, "=", pengurangan)

# perkalian
perkalian = nilai1 * nilai2
print(nilai1, "x", nilai2, "=", perkalian)

# pembagian
pembagian = nilai1 / nilai2
